// ----------------------------------------------------------------
function isTooMuchCaps(text) {
  if (!text) return false;
  const letters = text.replace(/[^a-zA-Zа-яА-ЯёЁ]/g, '');
  if (letters.length < 25) return false;
  const upper = letters.replace(/[^A-ZА-ЯЁ]/g, '').length;
  return (upper / letters.length) >= 0.70;
}

