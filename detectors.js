function parseDuration(str) {
  if (!str) return null;
  str = str.trim().toLowerCase();
  const map = [
    [/^(\d+)s(?:ec)?$/, 1],
    [/^(\d+)m(?:in)?$/, 60],
    [/^(\d+)h(?:r|our)?$/, 3600],
    [/^(\d+)d(?:ay)?$/, 86400],
    [/^(\d+)w(?:eek)?$/, 604800],
    [/^(\d+)мин?$/, 60],
    [/^(\d+)ч(?:ас)?$/, 3600],
    [/^(\d+)д(?:ень|ня|ней)?$/, 86400],
    [/^(\d+)н(?:едел)?$/, 604800],
  ];
  for (const [rx, mult] of map) {
    const m = str.match(rx);
    if (m) return parseInt(m[1]) * mult;
  }
  const n = parseInt(str);
  return isNaN(n) ? null : n * 60;
}

function fmtDur(sec) {
  if (!sec) return 'навсегда';
  const d = Math.floor(sec / 86400),
        h = Math.floor((sec % 86400) / 3600),
        m = Math.floor((sec % 3600) / 60),
        s = sec % 60;
  return [d && `${d}д`, h && `${h}ч`, m && `${m}м`, (!d && !h && s) && `${s}с`]
    .filter(Boolean).join(' ') || '0с';
}

function fmtTime(ts) {
  if (!ts) return 'никогда';
  return new Date(ts * 1000).toLocaleString('ru-RU', { timeZone: 'Europe/Moscow' });
}

function timeLeft(ts) {
  if (!ts) return 'навсегда';
  const d = ts - Math.floor(Date.now() / 1000);
  return d <= 0 ? 'истекло' : fmtDur(d);
}

function esc(t) {
  return t ? String(t).replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;') : '';
}

function mention(uid, name) {
  return `<a href="tg://user?id=${uid}">${esc(name || 'Пользователь')}</a>`;
}

