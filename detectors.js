function isAdvertising(text, chatUsername) {
  if (!text) return false;
  // t.me/ ссылки
  const tmeLinks = text.match(/(?:https?:\/\/)?t\.me\/([+a-zA-Z0-9_]+)/gi);
  if (tmeLinks) {
    for (const link of tmeLinks) {
      const handle = link.replace(/.*t\.me\//i, '').replace(/^\+/, '');
      if (chatUsername && handle.toLowerCase() === chatUsername.replace('@','').toLowerCase()) continue;
      if (/^\d+$/.test(handle)) continue;
      return true;
    }
  }
  if (/telegram\.me\/[a-zA-Z0-9_]{3,}/i.test(text)) return true;
  // Прямые призывы с @username
  if (/(?:подпишись|вступай|заходи|заходите|присоединяйся|подписывайтесь|жми|жмите)\s+(?:в|на|к|@)/i.test(text) &&
      /@[a-zA-Z0-9_]{4,}/.test(text)) return true;
  return false;
}

